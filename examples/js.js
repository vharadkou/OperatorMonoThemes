
/**
 * @param {string} newBalance 
 * @returns {string}
 */
function getBalance(newBalance) {
    this.myBankBalance = newValue;

    let output = "";

    // Do the 'for' loop
    for (myBankBalance = 0; myBankBalance <= 10; myBankBalance++) {
        if (myBankBalance === 5) {
            break;
        }
        output += "My bank balance is now $" + myBankBalance + "<br>";
    }

    return output;
}






